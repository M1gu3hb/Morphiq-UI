/**
 * Assembles `src/registry/generated.ts` from `src/registry/entries/*.ts`.
 *
 * Why codegen instead of a runtime glob: the catalog and `/components/[slug]`
 * are statically prerendered, and Turbopack has no supported
 * `import.meta.glob`-style API, so the module graph has to be literal. Emitting
 * real `import` statements keeps every entry statically analysable — tree
 * shaking, `generateStaticParams` and type checking all behave exactly as they
 * did when the array was hand written.
 *
 * Why it exists at all: adding a component used to mean editing
 * `src/registry/index.ts`, which is the one file every author adding a
 * component had to touch — a guaranteed conflict when two of them work in
 * parallel. Now a component ships its own `entries/<slug>.ts` and nothing
 * shared changes.
 *
 * The output is deliberately NOT committed (see `.gitignore`): a generated file
 * under version control would reintroduce exactly the shared-file conflict this
 * removes. It is regenerated by the `pre*` hooks in `package.json` before dev,
 * build, typecheck, the registry suite and the full gate.
 *
 * Run directly with `npm run registry:gen`.
 */

import { mkdirSync, readdirSync, readFileSync, realpathSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { pathToFileURL } from "node:url";

const root = process.cwd();
const entriesDir = join(root, "src", "registry", "entries");
const outputPath = join(root, "src", "registry", "generated.ts");

/** `entries/<slug>.ts` — the file name is the slug. */
const entryFilePattern = /^([a-z0-9]+(?:-[a-z0-9]+)*)\.ts$/;

export function readEntrySlugs() {
  let files;
  try {
    files = readdirSync(entriesDir);
  } catch {
    throw new Error(`Registry entries directory not found: src/registry/entries`);
  }

  const slugs = [];
  for (const file of files.sort()) {
    // Anything that is not a plain `.ts` file is ignored rather than fatal.
    // This directory collects incidental company — `README.md`, `.DS_Store`,
    // a `__tests__/` folder, and especially the `*.orig` / `*.rej` left behind
    // by a conflicted merge, which is exactly the situation parallel authoring
    // makes likely. Throwing on those would hard-block dev, build and the gate
    // over a file nobody meant to register.
    if (!file.endsWith(".ts") || file.endsWith(".d.ts")) continue;

    // A `.ts` file here IS meant to be an entry, so a name that cannot be a
    // slug is a real mistake (`Button.ts`, `nav_bar.ts`) and still fails loudly.
    const match = entryFilePattern.exec(file);
    if (!match) {
      throw new Error(
        `Registry entry file must be named <slug>.ts in kebab-case: src/registry/entries/${file}`,
      );
    }
    slugs.push(match[1]);
  }
  if (slugs.length === 0) throw new Error("No registry entries found in src/registry/entries");
  // Sorted by file name, which is the slug, so the assembled array is ordered
  // without anyone having to maintain the order by hand.
  return slugs.sort();
}

/**
 * Identifier-safe local name: `nav-bar` -> `entry_nav_bar`.
 *
 * The hyphen becomes `_` rather than being dropped with a capitalised next
 * part. Capitalisation is a no-op on digits, so `card-2` and `card2` would both
 * have collapsed to `entryCard2` — two valid, distinct slugs emitting the same
 * import binding, which surfaces much later as a `TS2300: Duplicate identifier`
 * against a generated file neither author wrote. Slugs are `[a-z0-9-]` only, so
 * `_` cannot occur in one and the mapping is injective.
 */
function localName(slug) {
  return `entry_${slug.split("-").join("_")}`;
}

export function renderRegistryModule(slugs) {
  // Belt and braces: even a future change to `localName` cannot silently emit a
  // duplicate binding.
  const names = slugs.map(localName);
  const duplicate = names.find((name, index) => names.indexOf(name) !== index);
  if (duplicate) {
    throw new Error(
      `Registry slugs collide on the generated identifier "${duplicate}": ${slugs.join(", ")}`,
    );
  }

  const imports = slugs
    .map((slug) => `import { entry as ${localName(slug)} } from "@/registry/entries/${slug}";`)
    .join("\n");
  const members = slugs.map((slug) => `  ${localName(slug)},`).join("\n");

  return `// AUTO-GENERATED by scripts/gen-registry.mjs — do not edit, do not commit.
// Add a component by creating src/registry/entries/<slug>.ts; this file is
// rebuilt from that directory before dev, build, typecheck and the gate.
import type { RegistryEntry } from "@/registry/schema";
${imports}

/** Every registry entry, ordered by slug. */
export const registry: RegistryEntry[] = [
${members}
];
`;
}

export function generate() {
  const slugs = readEntrySlugs();
  const contents = renderRegistryModule(slugs);
  let current = null;
  try {
    current = readFileSync(outputPath, "utf8");
  } catch {
    current = null;
  }
  if (current !== contents) {
    mkdirSync(dirname(outputPath), { recursive: true });
    writeFileSync(outputPath, contents, "utf8");
  }
  return { changed: current !== contents, contents, slugs };
}

// Only run when invoked as a script, so the suite can import the helpers above.
//
// `pathToFileURL` rather than hand-built `file://` strings: Windows paths and
// spaces both break naive concatenation. `realpathSync` because Node resolves
// symlinks for `import.meta.url` but only `path.resolve`s `argv[1]` — without
// it, a checkout reached through a junction, `subst` or a symlinked path makes
// this guard false and the script becomes a silent no-op that exits 0 without
// writing anything, taking every `pre*` hook down with it.
if (process.argv[1] && import.meta.url === pathToFileURL(realpathSync(process.argv[1])).href) {
  const { changed, slugs } = generate();
  process.stdout.write(
    `${JSON.stringify({ entries: slugs.length, slugs, written: changed })}\n`,
  );
}
